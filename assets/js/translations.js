// Función para obtener el idioma actual
function getCurrentLanguage() {
  return localStorage.getItem("language") || "en";
}

// Función para cambiar el idioma
function changeLanguage(lang) {
  localStorage.setItem("language", lang);
  updatePageLanguage(lang);
}

// Función para actualizar todo el contenido de la página
function updatePageLanguage(lang) {
  const t = translations[lang];

  // Actualizar navbar
  document.querySelector('a[href="#home"]').textContent = t.nav_home;
  document.querySelector('a[href="#project"]').textContent = t.nav_projects;
  document.querySelector('a[href="#about"]').textContent = t.nav_about;
  document.querySelector('a[href="#contact"]').textContent = t.nav_contact;

  // Actualizar hero
  document.querySelector(".availability .text").textContent = t.available;
  document.querySelector(".location").innerHTML = `
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 384 512" class="icon" width="20" height="20" fill="currentColor">
      <path d="M215.7 499.2C267 435 384 279.4 384 192C384 86 298 0 192 0S0 86 0 192c0 87.4 117 243 168.3 307.2c12.3 15.3 35.1 15.3 47.4 0zM192 128a64 64 0 1 1 0 128 64 64 0 1 1 0-128z"/>
    </svg>
    ${t.location}
  `;
  document.querySelector(
    ".escritura"
  ).innerHTML = `${t.hero_title} <span id="escritura"></span>`;

  // Actualizar proyectos
  document.querySelector(".project h1").textContent = t.projects_title;

  const projectTitles = document.querySelectorAll(".card__title");
  const projectDescs = document.querySelectorAll(".card__body");
  const projectButtons = document.querySelectorAll(".card__button");

  if (projectTitles[0]) projectTitles[0].textContent = t.project1_title;
  if (projectTitles[1]) projectTitles[1].textContent = t.project2_title;
  if (projectTitles[2]) projectTitles[2].textContent = t.project3_title;

  if (projectDescs[0]) projectDescs[0].textContent = t.project1_desc;
  if (projectDescs[1]) projectDescs[1].textContent = t.project2_desc;
  if (projectDescs[2]) projectDescs[2].textContent = t.project3_desc;

  projectButtons.forEach((btn) => (btn.textContent = t.project_github));

  // Actualizar Stats
  const statProjects = document.querySelector(".stat-projects");
  const statExperience = document.querySelector(".stat-experience");
  const statTechnologies = document.querySelector(".stat-technologies");
  const statDedication = document.querySelector(".stat-dedication");
  if (statProjects) statProjects.textContent = t.stats_projects;
  if (statExperience) statExperience.textContent = t.stats_experience;
  if (statTechnologies) statTechnologies.textContent = t.stats_technologies;
  if (statDedication) statDedication.textContent = t.stats_dedication;

  // Actualizar About
  document.querySelector(".about-me .main-title h1").textContent =
    t.about_title;
  const aboutText = document.querySelector(".about-content .about-text");
  if (aboutText) aboutText.innerHTML = t.about_text;

  // Actualizar CTA
  const ctaTitle = document.querySelector(".cta .title");
  const ctaSubtitle = document.querySelector(".cta .subtitle");
  if (ctaTitle) ctaTitle.textContent = t.cta_title;
  if (ctaSubtitle) ctaSubtitle.textContent = t.cta_subtitle;

  // Actualizar Skills
  document.querySelector(".skill-container .main-title h1").textContent =
    t.skills_title;

  const programmingTitle = document.querySelector(".skills-programming-title");
  const designingTitle = document.querySelector(".skills-designing-title");
  if (programmingTitle) programmingTitle.textContent = t.skills_programming;
  if (designingTitle) designingTitle.textContent = t.skills_designing;

  // Actualizar Education/Timeline
  const educationTitle = document.querySelector(".education h1");
  if (educationTitle) educationTitle.textContent = t.education_title;

  const timeline1Title = document.querySelector(".timeline-title-1");
  const timeline1Desc = document.querySelector(".timeline-desc-1");
  const timeline2Title = document.querySelector(".timeline-title-2");
  const timeline2Desc = document.querySelector(".timeline-desc-2");
  const timeline3Title = document.querySelector(".timeline-title-3");
  const timeline3Desc = document.querySelector(".timeline-desc-3");

  if (timeline1Title) timeline1Title.textContent = t.timeline1_title;
  if (timeline1Desc) timeline1Desc.textContent = t.timeline1_desc;
  if (timeline2Title) timeline2Title.textContent = t.timeline2_title;
  if (timeline2Desc) timeline2Desc.textContent = t.timeline2_desc;
  if (timeline3Title) timeline3Title.textContent = t.timeline3_title;
  if (timeline3Desc) timeline3Desc.textContent = t.timeline3_desc;

  // Actualizar Contact
  document.querySelector(".app-title").textContent = t.contact_title;

  const contactHeaderTitle = document.querySelector(".contact-header-title");
  const contactHeaderSubtitle = document.querySelector(
    ".contact-header-subtitle"
  );
  if (contactHeaderTitle)
    contactHeaderTitle.textContent = t.contact_header_title;
  if (contactHeaderSubtitle)
    contactHeaderSubtitle.textContent = t.contact_header_subtitle;

  const formLabelName = document.querySelector(".form-label-name");
  const formLabelEmail = document.querySelector(".form-label-email");
  const formLabelPhone = document.querySelector(".form-label-phone");
  const formLabelSubject = document.querySelector(".form-label-subject");
  const formLabelMessage = document.querySelector(".form-label-message");
  const formBtnReset = document.querySelector(".form-btn-reset");
  const formBtnSubmit = document.querySelector(".form-btn-submit");

  if (formLabelName) formLabelName.textContent = t.contact_name;
  if (formLabelEmail) formLabelEmail.textContent = t.contact_email;
  if (formLabelPhone) formLabelPhone.textContent = t.contact_phone;
  if (formLabelSubject) formLabelSubject.textContent = t.contact_subject;
  if (formLabelMessage) formLabelMessage.textContent = t.contact_message;
  if (formBtnReset) formBtnReset.textContent = t.contact_reset;
  if (formBtnSubmit) formBtnSubmit.textContent = t.contact_submit;

  // Reiniciar el efecto de escritura
  palabras[0] = t.hero_typing;
  letraIndex = 0;
  escribir();
}


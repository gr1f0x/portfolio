// FunciÃ³n para actualizar todo el contenido de la pÃ¡gina
function updatePageLanguage(lang) {
  const t = translations[lang];

  // Sistema universal basado en data-translate
  // Encuentra todos los elementos con atributo data-translate y actualiza su contenido
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (t[key]) {
      // Si el elemento contiene HTML (como <br>), usar innerHTML
      if (key === "about_text" || key === "location") {
        if (key === "location") {
          // Para location, solo actualizar el span interno
          element.textContent = t[key];
        } else {
          element.innerHTML = t[key];
        }
      } else {
        element.textContent = t[key];
      }
    }
  });

  // Actualizar placeholders
  document
    .querySelectorAll("[data-translate-placeholder]")
    .forEach((element) => {
      const key = element.getAttribute("data-translate-placeholder");
      if (t[key]) {
        element.placeholder = t[key];
      }
    });

  // Actualizar niveles de tech automÃ¡ticamente (Expert, Advanced, Intermediate, Beginner)
  document.querySelectorAll(".tech-level.expert").forEach((el) => {
    if (t.tech_level_expert) el.textContent = t.tech_level_expert;
  });
  document.querySelectorAll(".tech-level.advanced").forEach((el) => {
    if (t.tech_level_advanced) el.textContent = t.tech_level_advanced;
  });
  document.querySelectorAll(".tech-level.intermediate").forEach((el) => {
    if (t.tech_level_intermediate) el.textContent = t.tech_level_intermediate;
  });
  document.querySelectorAll(".tech-level.beginner").forEach((el) => {
    if (t.tech_level_beginner) el.textContent = t.tech_level_beginner;
  });

  // Actualizar el efecto de escritura con el nuevo texto
  if (t.hero_typing && window.palabras && window.escribir) {
    window.palabras[0] = t.hero_typing;
    window.letraIndex = 0;
    window.escribir();
  }
}

